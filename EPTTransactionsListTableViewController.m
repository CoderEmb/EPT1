- (void)configureCell:(UITableViewCell *)cell atIndexPath:(NSIndexPath *)indexPath {
    Transactions *info = [fetchedResultsController objectAtIndexPath:indexPath];
    Accounts *associatedAccount = (Accounts*)info.iTrantoAcc;
    cell.textLabel.text = associatedAccount.sAccountName;
    cell.detailTextLabel.text = [NSString stringWithFormat:@"%@",
                                 info.iAmount];
}

